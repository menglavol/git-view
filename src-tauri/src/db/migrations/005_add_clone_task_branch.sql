-- ---------------------------------------------------------------------
-- 迁移 005：为克隆任务增加「目标分支」列
-- ---------------------------------------------------------------------
-- 背景：批量克隆此前只能克隆平台默认分支。本列让用户在克隆时选择分支，
--       克隆执行阶段据此拼接 `git clone --branch <branch>`。
--
-- 兼容性：新增为可空列，旧库执行本迁移后自动补列；历史任务该列为 NULL，
--         语义约定 NULL = 克隆默认分支（与旧行为完全一致，不破坏既有数据）。
-- ---------------------------------------------------------------------
ALTER TABLE clone_tasks ADD COLUMN branch TEXT;
